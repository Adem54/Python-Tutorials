# For döngüsü kullanarak 1'den 100'e kadar tek sayıları yazdıran kod yazın.

for sayi in range(100):
    if sayi % 2 != 0:
        print(sayi)

print("-------------------------------------")
for sayi1 in range(5, 10):  # 5 ten başlar 10 a kadar sayı üretir
    print(sayi1)
print("-------------------------------------")
for sayi2 in range(-5, 5):
    print(sayi2)
